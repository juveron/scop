#include "libft_math.h"

t_mat4		mat4_translate(t_mat4 m, t_vec3 vec)
{
	m.m[12] += vec.v[0];
	m.m[13] += vec.v[1];
	m.m[14] += vec.v[2];
	return (m);
}
