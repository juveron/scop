#include "libft_math.h"

t_mat4		new_mat4(float f)
{
	int		i;
	t_mat4	new;

	i = -1;
	while (++i < 16)
	{
		if (f == MAT_IDENTITY)
			new.m[i] = i % 5 == 0 ? 1 : 0;
		else
			new.m[i] = f;
	}
	return (new);
}
